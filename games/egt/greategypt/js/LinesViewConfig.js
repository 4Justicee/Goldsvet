function LinesViewConfig(lines, numReelImages, numReels, reelCoordX, reelCoordY, reelWidth, reelHeight, reelSpacing)
{
	this.lines = lines;
	this.numReelImages = numReelImages;
	this.numReels = numReels;
	this.reelCoordX = reelCoordX;
	this.reelCoordY = reelCoordY;
	this.reelWidth = reelWidth;
	this.reelHeight = reelHeight;
	this.reelSpacing = reelSpacing;
}